package com.timeyaa.flutter_editable_charts;

/**
 * 数据模型
 */
public class LineDataModel {

    /**
     * x 坐标
     */
    private double x;

    /**
     * y 坐标
     */
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
